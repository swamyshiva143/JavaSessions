package Practice;

public class WrapperClassess {

	public static void main(String[] args) {
		
		int x=123;
		System.out.println(x+123+123+145);
		
		String s= String.valueOf(x);
		System.out.println(s+123+1232+123);
		
		
		
		String l="143";
		System.out.println(l+123+123+145);
		
		int t=Integer.parseInt(l);
		System.out.println(t+123+123+145);
		
		double a=999.888;
		double b=777.666;
		System.out.println(a+b);
		String c=String.valueOf(a);
		String d=String.valueOf(b);
		
		System.out.println(c+d);
	}

}
