package ListConcept;

public class Teacher {

	String name;
	String subject;
	int age;
	int salary;
	double weight;
	
	Teacher(String name, String subject, int age, int salary, double weight){
		this.name=name;
		this.subject=subject;
		this.age=age;
		this.salary=salary;
		this.weight=weight;
		
	}
	
	
}
